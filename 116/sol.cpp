#include <bits/stdc++.h>
using namespace std;

int main() {
    vector<int> arr = {0x20,0x5e,0x7b,0xd2,0x59,0xb1,0x34,0x72,0x1b,0x69,0x61,0x3c,0x11,0x35,0x65,0x80,0x9,0x9d,0x9,0x3d,0x22,0x7b,0x1,0x9d,0x59,0xaa,0x2,0x6a,0x53,0xa7,0xb,0xcd,0x25,0xdf,0x1,0x9c};
    vector<int> brr = {0x24,0x16,0x1,0xb1,0xd,0x4d,0x1,0x13,0x1c,0x32,0x1,0xc,0x20,0x2,0x1,0xe1,0x2d,0x6c,0x6,0x59,0x11,0x17,0x35,0xfe,0xa,0x7a,0x32,0xe,0x13,0x6f,0x5,0xae,0xc,0x7a,0x61,0xe1};
    string result = "";

    for (int i = 0; i < 36; i++) {
        int c;
        switch(i % 4) {
            case 0:
                c = arr[i] + brr[i];
                break;
            case 1:
                c = arr[i] - brr[i];
                break;
            case 2:
                c = arr[i] * brr[i];
                break;
            case 3:
                c = arr[i] ^ brr[i];
                break;
        }
        result += (char)c;
    }

    cout << result;
}